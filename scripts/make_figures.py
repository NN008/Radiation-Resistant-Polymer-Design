def fps_matrix(smiles_list, radius, bits):
    vecs = []
    for s in smiles_list:
        v = fp_from_smiles(s, radius=radius, nBits=bits)
        if v is not None:
            vecs.append(v)
    if len(vecs):
        return np.vstack(vecs)
    return None

