def save_caption(fig_path, caption, source="Generated by the author using scikit-learn, RDKit, and matplotlib."):
    cap_path = Path(fig_path).with_name("captions.txt")
    with open(cap_path, "a") as f:
        f.write(f"{Path(fig_path).name}: {caption}\nSource: {source}\n\n")

def fp_from_smiles(smiles, radius=2, nBits=2048):
    m = Chem.MolFromSmiles(smiles)
    if m is None:
        return None
    fp = AllChem.GetMorganFingerprintAsBitVect(m, radius, nBits=nBits)
    vec = np.zeros(nBits, dtype=np.float32)
    for b in fp.GetOnBits():
        vec[b] = 1.0
    return vec

def newest_subdir(root: Path):
    subs = [p for p in root.glob("*") if p.is_dir()]
    if not subs:
        raise FileNotFoundError(f"No subdirectories found under {root}")
    subs.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    return subs[0]

def latest_matching(run_dir: Path, patterns):
    """Return newest file in run_dir that matches any of the relative patterns."""
    best = None
    for pat in patterns:
        for p in run_dir.glob(pat):
            if best is None or p.stat().st_mtime > best.stat().st_mtime:
                best = p
    return best

def scatter_with_fit(ax, x, y, title, xlabel, ylabel):
    ax.scatter(x, y, s=10, alpha=0.6)
    coeffs = np.polyfit(x, y, 1)
    xfit = np.linspace(np.min(x), np.max(x), 100)
    yfit = coeffs[0]*xfit + coeffs[1]
    ax.plot(xfit, yfit, linewidth=1.5)
    r2 = r2_score(y, np.polyval(coeffs, x))
    ax.set_title(f"{title}\n$R^2$ = {r2:.3f}")
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.grid(True, linestyle=":", linewidth=0.5)
    return r2

def read_json_if_exists(p: Path):
    if p and p.is_file():
        return json.loads(p.read_text())
    return None

def get_col(df, names):
    for n in names:
        if df is not None and n in df.columns:
            return df[n].dropna().values
    return None

def get_series(df, names):
    for n in names:
        if df is not None and n in df.columns:
            return df[n]
    return None

def smiles_for_depict(s: str) -> str:
    # Keep polymer wildcards explicit so they render: "*" -> "[*]"
    return s.replace("*", "[*]")

def load_estimator(pathlike):
    obj = joblib.load(str(Path(pathlike).expanduser()))
    if isinstance(obj, dict):
        est = obj.get("model", obj.get("estimator", obj))
    elif isinstance(obj, (list, tuple)):
        est = obj[0]
    else:
        est = obj
    if not hasattr(est, "predict"):
        raise TypeError(f"{pathlike} did not load to a scikit-learn estimator.")
    return est

