def find_scaler_and_meta(proj_root: Path, runs_root: Path):
    # Preferred: retrained models folder
    candidates = []
    retrained = proj_root / "2Predictors" / "Retrained" / "models"
    if retrained.is_dir():
        candidates.append(retrained)

    # Fallback: newest models_snapshot under Runs/*/<models_snapshot>
    snapshots = sorted((p for p in runs_root.glob("*/*") if p.name == "models_snapshot"),
                       key=lambda p: p.stat().st_mtime, reverse=True)
    candidates.extend(snapshots)

    for p in candidates:
        sp = p / "desc_scaler.pkl"
        mp = p / "metadata.json"
        if sp.is_file():
            return sp, (mp if mp.is_file() else None)
    return None, None

def load_feature_order(meta_path: Path, df_desc: pd.DataFrame):
    if meta_path and meta_path.is_file():
        try:
            J = json.loads(meta_path.read_text())
            for key in ["feature_names", "desc_names", "descriptor_names"]:
                if key in J and isinstance(J[key], list) and len(J[key]) > 0:
                    return [c for c in J[key] if c in df_desc.columns]
        except Exception:
            pass
    ban = {"SMILES", "smiles", "Tg", "Tg_C", "tg", "tg_c", "MAC", "MAC_cm2_g", "mac", "mac_cm2_g"}
    return [c for c in df_desc.columns if c not in ban]

def build_descriptor_matrix_for_training(proj_root: Path, train_df: pd.DataFrame, smiles_col: str):
    cache_path = proj_root / "data" / "desc_cache.csv"
    if not cache_path.is_file():
        return None, None
    df_desc = pd.read_csv(cache_path)
    desc_smi = "SMILES" if "SMILES" in df_desc.columns else ("smiles" if "smiles" in df_desc.columns else None)
    if desc_smi is None:
        return None, None
    merged = pd.merge(train_df, df_desc, left_on=smiles_col, right_on=desc_smi, how="inner")
    if len(merged) == 0:
        return None, None
    return merged, df_desc

