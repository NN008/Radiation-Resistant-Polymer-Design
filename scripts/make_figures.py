def boxplot_three(ax, train_vals, gen_vals, hit_vals, title, ylab, thresholds=None):
    data, labels = [], []
    if train_vals is not None and len(train_vals): data.append(train_vals); labels.append("Training")
    if gen_vals   is not None and len(gen_vals):   data.append(gen_vals);   labels.append("All generated")
    if hit_vals   is not None and len(hit_vals):   data.append(hit_vals);   labels.append("Top hits")
    if len(data) == 0:
        ax.axis('off'); ax.text(0.5,0.5,"No data", ha='center'); return
    ax.boxplot(data, labels=labels, showfliers=False)
    ax.set_title(title); ax.set_ylabel(ylab); ax.grid(True, linestyle=":", linewidth=0.5)
    if thresholds:
        for t in thresholds: ax.axhline(t, linestyle="--", linewidth=1)

